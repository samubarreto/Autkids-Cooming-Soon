// Lista de chaves que precisam suportar HTML (whitelist)
const KEYS_WITH_HTML = ['hero_title'];

// Classes CSS permitidas em elementos HTML sanitizados
const ALLOWED_SPAN_CLASS = 'highlight';

// Função para sanitizar HTML permitindo apenas tags seguras
function sanitizeHTML(html) {
  try {
    // Usa DOMParser para parsing mais seguro
    const parser = new DOMParser();
    const doc = parser.parseFromString(html, 'text/html');
    
    // Verifica se o parsing foi bem-sucedido
    if (!doc || !doc.body) {
      // Fallback: retorna apenas o conteúdo de texto
      const temp = document.createElement('div');
      temp.textContent = html;
      return temp.textContent;
    }
    
    // Função recursiva para validar e limpar elementos
    function sanitizeNode(node) {
      // Se for um nó de texto, retornar o texto
      if (node.nodeType === Node.TEXT_NODE) {
        return document.createTextNode(node.textContent);
      }
      
      // Se for um elemento, validar tag e atributos
      if (node.nodeType === Node.ELEMENT_NODE) {
        const tagName = node.tagName.toLowerCase();
        
        // Apenas permitir span e br
        if (tagName === 'span') {
          // Validar que span tem APENAS um atributo e que é class="highlight"
          const hasOnlyClassAttribute = node.attributes.length === 1;
          const hasCorrectClass = node.getAttribute('class') === ALLOWED_SPAN_CLASS;
          
          if (hasOnlyClassAttribute && hasCorrectClass) {
            const newSpan = document.createElement('span');
            newSpan.className = ALLOWED_SPAN_CLASS;
            // Processar filhos recursivamente
            Array.from(node.childNodes).forEach(child => {
              const sanitizedChild = sanitizeNode(child);
              if (sanitizedChild) {
                newSpan.comendChild(sanitizedChild);
              }
            });
            return newSpan;
          }
          // Se span não tem atributos corretos, retornar apenas o conteúdo de texto
          return document.createTextNode(node.textContent);
        } else if (tagName === 'br') {
          // br não deve ter nenhum atributo
          if (node.attributes.length === 0) {
            return document.createElement('br');
          }
          // Se br tem atributos, retornar como texto para consistência
          return document.createTextNode(node.textContent);
        }
        
        // Qualquer outra tag: retornar apenas texto
        return document.createTextNode(node.textContent);
      }
      
      // Qualquer outro tipo de nó: ignorar
      return null;
    }
    
    // Criar container para o resultado sanitizado
    const result = document.createElement('div');
    Array.from(doc.body.childNodes).forEach(child => {
      const sanitizedChild = sanitizeNode(child);
      if (sanitizedChild) {
        result.comendChild(sanitizedChild);
      }
    });
    
    return result.innerHTML;
  } catch (error) {
    // Em caso de erro, retornar apenas o conteúdo de texto (fallback seguro)
    console.error('Erro ao sanitizar HTML:', error);
    const temp = document.createElement('div');
    temp.textContent = html;
    return temp.textContent;
  }
}

// Função para trocar o idioma
function changeLanguage(lang) {
  // 1. Salva a preferência do usuário no navegador
  localStorage.setItem('autkids-language', lang);

  // 2. Atualiza o título da página
  if (translations[lang] && translations[lang]['page_title']) {
    document.title = translations[lang]['page_title'];
  }

  // 3. Atualiza os textos na página
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    
    // Verifica se existe tradução para essa chave
    if (translations[lang] && translations[lang][key]) {
      // Usa textContent por padrão (seguro contra XSS)
      // Apenas chaves específicas na whitelist podem usar HTML sanitizado
      if (KEYS_WITH_HTML.includes(key)) {
        element.innerHTML = sanitizeHTML(translations[lang][key]);
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
}

// Ao carregar a página, verifica se já tem idioma salvo
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('autkids-language') || 'pt'; // Padrão PT
  changeLanguage(savedLang);
});